/* Task#05:

   Write a C++ program to declare two integers, one float 
   variable and assign 10, 15, and 12.6 to them respectively. 
   It then prints these values on the screen.  */
   
#include<iostream>
using namespace std;
int main()
{
	int x;
	int y;
	float z;
	
	x = 10;
	y = 15;
	z = 12.6;
	
	cout<<"First Integer Variable: \n"<<x<<endl;
    
	cout<<"Second Integer Variable: \n"<<y<<endl;
	
	cout<<"First Float Variable: \n"<<z<<endl;
	
	return 0;
}

