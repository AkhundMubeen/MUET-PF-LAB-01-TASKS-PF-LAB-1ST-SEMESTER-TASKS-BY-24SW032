#include<iostream>
using namespace std;
int main()
{
	
	cout<<"The Size Of Fundamental Data Types Is Given:"<<endl<<endl;
	
	cout<<"The Size Of Integer (int) Data Type Is: "<<sizeof(int)<<" Bytes\n";
	cout<<"The Size Of Float (float) Data Type Is: "<<sizeof(float)<<" Bytes\n";
	cout<<"The Size Of Character (char) Data Type Is: "<<sizeof(char)<<" Byte\n";
	cout<<"The Size Of Double (double) Data Type Is: "<<sizeof(double)<<" Bytes\n";
	cout<<"The Size Of Boolean (bool) Data Type Is: "<<sizeof(bool)<<" Bytes\n";
	cout<<"The Size Of Text (string) Data Type Is: "<<sizeof(string)<<" Bytes\n";
	return 0;
	
}

